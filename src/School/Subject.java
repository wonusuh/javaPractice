package School;

public class Subject {

	int stuNo, score;
	String subName;

	int getStuNo() {
		return stuNo;
	}

	void setStuNo(int stuNo) {
		this.stuNo = stuNo;
	}

	int getScore() {
		return score;
	}

	void setScore(int score) {
		this.score = score;
	}

	String getSubName() {
		return subName;
	}

	void setSubName(String subName) {
		this.subName = subName;
	}
}
