package 기초1개념1;
public class _03출력문 {
	public static void main(String[] args) {
		System.out.println("안녕하세요");
		// syso + ctrl + space
		System.out.println("반갑습니다");
		System.out.println("test");
		System.out.println("\"test\"");
		System.out.println('t');
		System.out.print("test");
		System.out.println("'test'");
		System.out.println("'te\nst'");
		System.out.println("'te\nst\\'");
	}
}
