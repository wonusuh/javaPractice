package ATM;

public class Account {
	int money;
	String id, accNumber;

	int getMoney() {
		return money;
	}

	void setMoney(int money) {
		this.money = money;
	}

	String getId() {
		return id;
	}

	void setId(String id) {
		this.id = id;
	}

	String getAccNumber() {
		return accNumber;
	}

	void setAccNumber(String accNumber) {
		this.accNumber = accNumber;
	}
}
