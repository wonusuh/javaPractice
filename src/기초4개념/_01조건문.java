package 기초4개념;

// 제어문 : 조건문, 반복문
public class _01조건문 {
	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		System.out.println("1");
		if (true) {
			System.out.println("실행");
		}
		System.out.println("2");
		if (a == b) {
			System.out.println("실행xxx");
		}
		System.out.println("3");
		System.out.println("1");
		if (true) {
			System.out.println("실행");
		}
		System.out.println("2");
		if (a != b) {
			System.out.println("실행xxx");
		}
		System.out.println("3");
	}
}
