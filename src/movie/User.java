package movie;

public class User {

	String id, pw, name;

	String getId() {
		return id;
	}

	void setId(String id) {
		this.id = id;
	}

	String getPw() {
		return pw;
	}

	void setPw(String pw) {
		this.pw = pw;
	}

	String getName() {
		return name;
	}

	void setName(String name) {
		this.name = name;
	}
}
