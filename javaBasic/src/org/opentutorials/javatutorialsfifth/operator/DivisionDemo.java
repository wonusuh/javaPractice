package org.opentutorials.javatutorialsfifth.operator;

public class DivisionDemo {
	public static void main(String[] args) {
		int a = 10;
		int b = 3;
		float c = (float) 10.0;
		float d = (float) 3.0;
		System.out.println(a / b); // 3
		System.out.println(c / d); // 3.3333....
		System.out.println(a / d); // 3.3333....
	}
}
