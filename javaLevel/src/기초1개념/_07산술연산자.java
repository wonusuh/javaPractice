package 기초1개념;

public class _07산술연산자 {
	public static void main(String[] args) {
		System.out.println(10 + 3);
		System.out.println(10 - 3);
		System.out.println(10 * 3);
		System.out.println(10 / 3);
		System.out.println(10.0 / 3);
		System.out.println(10 / 3.0);
		System.out.println(10.0 / 3.0);
		System.out.println(10 % 3);
	}
}
